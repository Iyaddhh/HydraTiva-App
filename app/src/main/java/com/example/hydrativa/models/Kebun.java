package com.example.hydrativa.models;

public class Kebun {
    private String namaKebun;
    private String luasLahan;
    private String lokasiKebun;
    private String kodeAlat;

    public String getNamaKebun() {
        return namaKebun;
    }

    public void setNamaKebun(String namaKebun) {
        this.namaKebun = namaKebun;
    }

    public String getLuasLahan() {
        return luasLahan;
    }

    public void setLuasLahan(String luasLahan) {
        this.luasLahan = luasLahan;
    }

    public String getLokasiKebun() {
        return lokasiKebun;
    }

    public void setLokasiKebun(String lokasiKebun) {
        this.lokasiKebun = lokasiKebun;
    }

    public String getKodeAlat() {
        return kodeAlat;
    }

    public void setKodeAlat(String kodeAlat) {
        this.kodeAlat = kodeAlat;
    }
}
